"""Tests for Go code generator."""

import pytest
from picomsg.schema.ast import (
    Schema, Namespace, Struct, Message, Field, Enum, EnumValue,
    PrimitiveType, StringType, BytesType, ArrayType, FixedArrayType,
    StructType, EnumType,
)
from picomsg.codegen.go import GoCodeGenerator


def _generate(schema):
    gen = GoCodeGenerator(schema)
    files = gen.generate()
    assert len(files) == 1
    content = list(files.values())[0]
    return content


def _make_schema(**kwargs):
    defaults = dict(enums=[], structs=[], messages=[], namespace=None)
    defaults.update(kwargs)
    return Schema(**defaults)


# ── Basic generation ──────────────────────────────────────────────


class TestBasicGeneration:
    def test_single_file_output(self):
        schema = _make_schema(namespace=Namespace("test.example"))
        gen = GoCodeGenerator(schema)
        files = gen.generate()
        assert len(files) == 1
        assert "picomsg_generated.go" in files

    def test_custom_module_name(self):
        schema = _make_schema()
        gen = GoCodeGenerator(schema)
        gen.set_option('module_name', 'my_api')
        files = gen.generate()
        assert "my_api.go" in files
        assert "package my_api" in files["my_api.go"]

    def test_package_declaration(self):
        content = _generate(_make_schema())
        assert "package picomsg_generated" in content

    def test_imports(self):
        content = _generate(_make_schema())
        assert '"bytes"' in content
        assert '"encoding/binary"' in content
        assert '"io"' in content
        assert '"fmt"' in content

    def test_auto_generated_comment(self):
        content = _generate(_make_schema())
        assert "Code generated by PicoMsg. DO NOT EDIT." in content

    def test_namespace_prefix_constants(self):
        schema = _make_schema(namespace=Namespace("test.example"))
        content = _generate(schema)
        assert "TestExampleMagicByte1" in content
        assert "TestExampleVersion" in content
        assert "TestExampleHeaderSize" in content

    def test_no_namespace(self):
        schema = _make_schema(
            structs=[Struct("Point", [Field("x", PrimitiveType("f32"))])]
        )
        content = _generate(schema)
        assert "type Point struct" in content
        assert "MagicByte1" in content

    def test_version_constant(self):
        schema = _make_schema(namespace=Namespace("api"), version=3)
        content = _generate(schema)
        assert "ApiVersion uint8 = 3" in content

    def test_serializer_interface(self):
        content = _generate(_make_schema())
        assert "Serializer interface" in content
        assert "ToBytes() ([]byte, error)" in content
        assert "FromBytes(data []byte) error" in content

    def test_error_variables(self):
        schema = _make_schema(namespace=Namespace("test.example"))
        content = _generate(schema)
        assert "ErrTestExampleInvalidHeader" in content
        assert "ErrTestExampleBufferTooSmall" in content
        assert "ErrTestExampleInvalidData" in content

    def test_helper_functions(self):
        content = _generate(_make_schema())
        assert "func readPrimitive[T any](r io.Reader)" in content
        assert "func writePrimitive[T any](w io.Writer, v T)" in content
        assert "func readString(r io.Reader)" in content
        assert "func writeString(w io.Writer, s string)" in content
        assert "func readBytes(r io.Reader)" in content
        assert "func writeBytes(w io.Writer, data []byte)" in content
        assert "func readBool(r io.Reader)" in content
        assert "func writeBool(w io.Writer, v bool)" in content


