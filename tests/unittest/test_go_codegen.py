        schema = _make_schema(
            enums=[Enum("priority", PrimitiveType("u8"), [
                EnumValue("Low", 1), EnumValue("High", 10),
            ])],
            structs=[Struct("task", [Field("prio", EnumType("priority"))])]
        )
