    def test_enum_backing_types(self):
        for backing in ["u8", "u16", "u32", "u64", "i8", "i16", "i32", "i64"]:
            go_type = backing.replace("u", "uint").replace("i", "int")
            if backing.startswith("u"):
                go_type = backing.replace("u", "uint")
            else:
                go_type = backing.replace("i", "int")
            schema = _make_schema(
                enums=[Enum("test_enum", PrimitiveType(backing), [EnumValue("Val", 1)])]
            )
            content = _generate(schema)
            assert f"type TestEnum {go_type}" in content


