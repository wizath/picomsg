# ── Complex schemas ───────────────────────────────────────────────


class TestComplexSchemas:
    def test_all_field_types_together(self):
        schema = _make_schema(
            namespace=Namespace("test"),
            enums=[Enum("color", PrimitiveType("u8"), [
                EnumValue("Red", 0), EnumValue("Green", 1), EnumValue("Blue", 2),
            ])],
            structs=[
                Struct("point", [
                    Field("x", PrimitiveType("f32")),
                    Field("y", PrimitiveType("f32")),
                ]),
                Struct("complex", [
                    Field("id", PrimitiveType("u64")),
                    Field("name", StringType()),
                    Field("data", BytesType()),
                    Field("flag", PrimitiveType("bool")),
                    Field("pos", StructType("point")),
                    Field("shade", EnumType("color")),
                    Field("scores", ArrayType(PrimitiveType("f64"))),
                    Field("tags", FixedArrayType(StringType(), 3)),
                    Field("matrix", FixedArrayType(PrimitiveType("u8"), 4)),
                ]),
            ]
        )
        content = _generate(schema)
        # Verify all types present
        assert "Id uint64" in content
        assert "Name string" in content
        assert "Data []byte" in content
        assert "Flag bool" in content
        assert "Pos TestPoint" in content
        assert "Shade TestColor" in content
        assert "Scores []float64" in content
        assert "Tags [3]string" in content
        assert "Matrix [4]uint8" in content

    def test_enum_array(self):
        schema = _make_schema(
            enums=[Enum("priority", PrimitiveType("u8"), [
                EnumValue("Low", 0), EnumValue("High", 1),
            ])],
            structs=[Struct("task_list", [
                Field("priorities", ArrayType(EnumType("priority"))),
            ])]
        )
        content = _generate(schema)
        assert "Priorities []Priority" in content
        assert "PriorityFromValue(v)" in content

    def test_fixed_enum_array(self):
        schema = _make_schema(
            enums=[Enum("priority", PrimitiveType("u8"), [
                EnumValue("Low", 0), EnumValue("High", 1),
            ])],
            structs=[Struct("task_triple", [
                Field("priorities", FixedArrayType(EnumType("priority"), 3)),
            ])]
        )
        content = _generate(schema)
        assert "Priorities [3]Priority" in content

    def test_fixed_struct_array(self):
        schema = _make_schema(
            namespace=Namespace("geo"),
            structs=[
                Struct("point", [Field("x", PrimitiveType("f32"))]),
                Struct("triangle", [
                    Field("vertices", FixedArrayType(StructType("point"), 3)),
                ]),
            ]
        )
        content = _generate(schema)
        assert "Vertices [3]GeoPoint" in content

    def test_struct_array_serialization(self):
        schema = _make_schema(
            namespace=Namespace("api"),
            structs=[
                Struct("item", [Field("id", PrimitiveType("u32"))]),
                Struct("container", [
                    Field("items", ArrayType(StructType("item"))),
                ]),
            ]
        )
        content = _generate(schema)
        # Write: call WriteTo on each item
        assert "item.WriteTo(w)" in content
        # Read: call ReadFrom on each item
        assert "ReadFrom(r)" in content


